/* Encrypt a single block with 10 rounds */
void AesEncrypt(unsigned char *blk, unsigned char *key, int Nr)
{
	printf("Round 0:\n");
	printf("-----Start:  ");
	for (int i = 0; i < 16; i++)
		printf("%02x ", blk[i]);

	printf("\n");
	printf("----Output:  ");
	AddRoundKey(blk, key, 0);

	for (int i = 0; i < 16; i++)
		printf("%02x ", blk[i]);

	for (int x = 1; x <= (Nr - 1); x++)
	{
		SubBytes(blk);
		ShiftRows(blk);
		MixColumns(blk);
		AddRoundKey(blk, key, x);
		printf("\nRound %d:\n", x);
		printf("----Output:  ");
		for (int i = 0; i < 16; i++)
			printf("%02x ", blk[i]);

	}

	printf("\nRound 10:\n");
	SubBytes(blk);
	ShiftRows(blk);
	AddRoundKey(blk, key, Nr);
	printf("----Output:  ");
	for (int i = 0; i < 16; i++)
		printf("%02x ", blk[i]);
}






