/* The *x function */
static unsigned char xtime(unsigned char x)
{
	if (x & 0x80) { return ((x << 1) ^ 0x1B) & 0xFF; }
	return x << 1;
}



/* MixColumns: Processes the entire block */
static void MixColumns(unsigned char *col)
{
	unsigned char tmp[4], xt[4];

	for (int x = 0; x < 4; x++, col += 4)
	{
		xt[0]  = xtime(col[0]);
		xt[1]  = xtime(col[1]);
		xt[2]  = xtime(col[2]);
		xt[3]  = xtime(col[3]);
		tmp[0] = xt[0]  ^ xt[1]  ^ col[1] ^ col[2] ^ col[3];
		tmp[1] = col[0] ^ xt[1]  ^ xt[2]  ^ col[2] ^ col[3];
		tmp[2] = col[0] ^ col[1] ^ xt[2]  ^ xt[3]  ^ col[3];
		tmp[3] = xt[0]  ^ col[0] ^ col[1] ^ col[2] ^ xt[3];
		col[0] = tmp[0];
		col[1] = tmp[1];
		col[2] = tmp[2];
		col[3] = tmp[3];
	}
}




/* ShiftRows: Shifts the entire block */
static void ShiftRows(unsigned char *col)
{
	unsigned char t;

	/* 2nd row */
	t       = col[1];
	col[1]  = col[5];
	col[5]  = col[9];
	col[9]  = col[13];
	col[13] = t;

	/* 3rd row */
	t       = col[2];
	col[2]  = col[10];
	col[10] = t;
	t       = col[6];
	col[6]  = col[14];
	col[14] = t;

	/* 4th row */
	t        = col[15];
	col[15]  = col[11];
	col[11]  = col[7];
	col[7]   = col[3];
	col[3]   = t;
}



/* SubBytes */
static void SubBytes(unsigned char *col)
{
#if DEMO
	for (int x = 0; x < 16; x++)
		col[x] = sbox[col[x]];
#else 	
	for (int x = 0; x < 16; x++)
		col[x] = modifiedsbox[col[x]];
#endif // DEMO
}



/* AddRoundKey */
static void AddRoundKey(unsigned char *col, unsigned char *key, int round)
{
	for (int x = 0; x < 16; x++)
		col[x] ^= key[(round << 4) + x];
}